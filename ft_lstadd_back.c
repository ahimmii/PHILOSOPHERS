#include "Philosophers.h"

void	ft_lstadd_back(t_philosophers **lst, t_philosophers *new)
{
	t_philosophers	*last;

	last = *lst;
	if (!new)
		return ;
	if (!*lst)
	{
		*lst = new;
		return ;
	}
	last = ft_lstlast(*lst);
	last->next = new;
}
